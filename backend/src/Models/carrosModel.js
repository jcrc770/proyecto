class CarrosModel {
  constructor(id, marca, modelo, year, tipo, capacidad, direccion, gasolina, precio){
    this.id=id
    this.marca=marca
    this.modelo=modelo
    this.year=year
    this.tipo=tipo
    this.capacidad=capacidad
    this.direccion=direccion
    this.gasolina=gasolina
    this.precio=precio
  }
}
export default CarrosModel